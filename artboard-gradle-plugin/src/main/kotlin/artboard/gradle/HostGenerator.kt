import java.io.File

/** Generates the isolated gallery entry point and browser resources. */
@CacheableTask
abstract class GenerateArtboardHostTask : DefaultTask() {
    @get:Input
    abstract val registryPackage: Property<String>

    @get:Input
    abstract val hostPackage: Property<String>

    @get:Input
    abstract val title: Property<String>

    @get:Input
    abstract val entryScript: Property<String>

    @get:InputFiles
    @get:PathSensitive(PathSensitivity.RELATIVE)
    abstract val composeResourceRoots: ConfigurableFileCollection

    @get:OutputDirectory
    abstract val outputDirectory: DirectoryProperty

    @TaskAction
    fun generate() {
        val output = outputDirectory.get().asFile
        output.deleteRecursively()
        HostGenerator.generate(
            outputDir = output,
            registryPackage = registryPackage.get(),
            hostPackage = hostPackage.get(),
            title = title.get(),
            languageTags = LocaleDiscovery.scanRoots(composeResourceRoots.files),
            entryScript = entryScript.get(),
        )
    }
}

/**
 * Writes plugin-owned gallery entry sources into [outputDir].
 *
 * Generated main uses [GeneratedArtboardRegistry] from [registryPackage]
 * and [ArtboardSupportedLocales] from project composeResources.
 */
internal object HostGenerator {

    fun generate(
        outputDir: File,
        registryPackage: String,
        hostPackage: String,
        title: String,
        languageTags: Collection<String>,
        entryScript: String,
    ) {
        val kotlinDir = File(outputDir, "kotlin/${hostPackage.replace('.', '/')}")
        val resourcesDir = File(outputDir, "resources")
        kotlinDir.mkdirs()
        resourcesDir.mkdirs()

        File(kotlinDir, "ArtboardSupportedLocales.kt").writeText(
            localesKt(hostPackage = hostPackage, languageTags = languageTags),
        )
        File(kotlinDir, "ArtboardHostMain.kt").writeText(
            mainKt(
                hostPackage = hostPackage,
                registryPackage = registryPackage,
                title = title,
            ),
        )
        File(resourcesDir, "index.html").writeText(indexHtml(entryScript, title))
    }

    private fun localesKt(
        hostPackage: String,
        languageTags: Collection<String>,
    ): String {
        val tagLiterals = languageTags.joinToString(", ") { "\"${it.replace("\"", "")}\"" }
        return """
            |// Generated by Artboard Gradle plugin — do not edit.
            |package $hostPackage
            |
            |import artboard.host.ArtboardLocale
            |import artboard.host.artboardLocalesFromResourceTags
            |
            |/**
            | * Locales discovered from this module's `composeResources/values*` folders.
            | * Used by the gallery locale control (System + each resource language).
            | */
            |object ArtboardSupportedLocales {
            |    private val resourceLanguageTags: List<String> = listOf($tagLiterals)
            |
            |    val all: List<ArtboardLocale> =
            |        artboardLocalesFromResourceTags(resourceLanguageTags)
            |}
            |
        """.trimMargin()
    }

